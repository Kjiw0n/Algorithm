N = list(map(int, input().split()))

STD_N = 1000000

for n in N:

    if n > STD_N:
        n1 = n // STD_N
        type_n = 3 if n1 % 3 == 0 else n1 % 3
        n2 = n % STD_N

        new_n = type_n * STD_N + n2
    else:
        new_n = n

    x, y = 0, 1

    for _ in range(new_n):
        x, y = y % 1000000, (x + y) % 1000000

    print(x)

